#include <iostream>


using namespace std;
//Faça um programa que leia um número inteiro e mostre o seu antecessor e seu sucessor.




int main(){
    int n1, ant, suc;

    cout << "Digite um número: ";
    cin >>n1;
    suc = n1 + 1;
    ant = n1 - 1;
    cout << "O Sucessor de " << n1 << " é " << suc<< endl;
    cout << "O Antecessor de " << n1 << " é " << ant;






return 0;

}
